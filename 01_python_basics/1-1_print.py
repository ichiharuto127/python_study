# ========================================
# 第1章: print関数
# ========================================

# 基本的な文字列の出力
print("Hello, World!")
print("Pythonの学習を始めよう！")

# 数値の出力
print(100)
print(3.14)

# 複数の値をカンマで区切って出力
print("身長:", 175, "cm")

# sep: 区切り文字を変更（デフォルトはスペース）
print("2026", "02", "23", sep="/")   # → 2026/02/23

# end: 末尾の文字を変更（デフォルトは改行）
print("Hello", end=" ")
print("World!")                       # → Hello World!

# 計算結果の出力
print(3 + 5)    # → 8
print(10 / 3)   # → 3.3333...
print(10 // 3)  # → 3  (整数除算)
print(10 % 3)   # → 1  (余り)
print(2 ** 8)   # → 256 (累乗)
